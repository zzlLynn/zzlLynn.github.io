let next0 = document.getElementById("next0")
$(function () {
    $(next0).on({
        mouseover: function () {
            let l1 = document.getElementById("next1")
            let l2 = document.getElementById("next2")
            $(l1).animate({
                opacity: 1
            }, 200);
            $(l2).animate({
                opacity: 0
            }, 200);
        },
        mouseout: function () {
            let l1 = document.getElementById("next1")
            let l2 = document.getElementById("next2")
            $(l1).animate({
                opacity: 0
            }, 200);
            $(l2).animate({
                opacity: 1
            }, 200);
        }
    });
});

