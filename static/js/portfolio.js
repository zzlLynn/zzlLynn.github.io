let fireworks_cover = document.getElementById("fireworks_cover")
$(function () {
    $(fireworks_cover).on({
        mouseover: function () {
            let l1 = document.getElementById("fireworks_highlight")
            $(l1).animate({
                width: "529.6px"
            }, 1000);
        },
    });
});
