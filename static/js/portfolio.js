let moos_cover = document.getElementById("moos_cover")
$(function () {
    $(moos_cover).on({
        mouseover: function () {
            setTimeout(() => {
                if ($(moos_cover).is(':hover')) {
                    let l1 = document.getElementById("moos_highlight")
                    $(l1).animate({
                        width: "239px"
                    }, 500);
                    moos_cover.style.transform = "scale(1.2)";
                }
            }, 500);
        },
    });
    $(moos_cover).on({
        mouseout: function () {
            let l1 = document.getElementById("moos_highlight")
            $(l1).animate({
                width: "0px"
            }, 500);
            moos_cover.style.transform = "scale(1)";
        },
    });
});
